namespace ElectronicStore.Utils
{
    public class ColourChanger
    {
        public static void GreenText() => Console.ForegroundColor = ConsoleColor.Green;

        public static void WhiteText() => Console.ForegroundColor = ConsoleColor.White;

        public static void RedText() => Console.ForegroundColor = ConsoleColor.Red;

        public static void BlueText() => Console.ForegroundColor = ConsoleColor.Blue;

        public static void YellowText() => Console.ForegroundColor = ConsoleColor.Yellow;
    }
}
